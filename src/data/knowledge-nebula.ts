export type KnowledgeNebulaSection = {
  id: string;
  title: string;
  summary: string;
  body: string[];
  embedding?: number[];
  relatedSectionIds?: string[];
  sourceUrl?: string | null;
  tags?: string[];
  viewCount?: number;
};

export type KnowledgeNebulaTopic = {
  slug: KnowledgeNebulaTopicSlug;
  title: string;
  shortLabel: string;
  summary: string;
  accent: "cyan" | "sky" | "indigo";
  featuredSectionIds: string[];
  sections: KnowledgeNebulaSection[];
};

type KnowledgeNebulaSectionSeed = Omit<
  KnowledgeNebulaSection,
  "embedding" | "relatedSectionIds" | "viewCount"
>;

type KnowledgeNebulaTopicSeed = Omit<KnowledgeNebulaTopic, "sections"> & {
  sections: KnowledgeNebulaSectionSeed[];
};

function buildSeedEmbedding(section: KnowledgeNebulaSectionSeed) {
  const text = [
    section.title,
    section.summary,
    ...(section.tags ?? []),
    ...section.body,
  ].join(" ");
  const dimensions = 24;
  const vector = Array.from({ length: dimensions }, () => 0);

  for (let index = 0; index < text.length; index += 1) {
    const code = text.charCodeAt(index);
    vector[index % dimensions] += ((code % 97) + 1) / 97;
  }

  const magnitude =
    Math.sqrt(vector.reduce((sum, value) => sum + value ** 2, 0)) || 1;
  return vector.map((value) => Number((value / magnitude).toFixed(6)));
}

function hydrateTopic(topic: KnowledgeNebulaTopicSeed): KnowledgeNebulaTopic {
  return {
    ...topic,
    sections: topic.sections.map((section) => ({
      ...section,
      embedding: buildSeedEmbedding(section),
    })),
  };
}

